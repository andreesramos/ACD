num = int(input("Introduce un numero entre el 1 y el 7: "));

if num==1:
    print("Lunes");
elif num==2:
    print("Martes");
elif num==3:
    print("Miercoles");
elif num==4:
    print("Jueves");
elif num==5:
    print("Viernes");
elif num==6:
    print("Sabado");
elif num==7:
    print("Domingo");
else:
    print("Vuelve a leer lo que pide el ejercicio.");
